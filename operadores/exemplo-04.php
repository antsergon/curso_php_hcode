<?php
//OPERADORES ARITMETICOS
    $valor = 30;
    $valor2 = 55;

    var_dump($valor > $valor2); //maior que $valor2

    echo "<br>";
    var_dump($valor < $valor2);// menor que $valor2

    echo "<br>";

    var_dump($valor == $valor2); // se sao iguais

    echo "<br>";

    var_dump($valor === $valor2); //se sao identicos

    echo "<br>";

    var_dump($valor != $valor2); //se sao diferentes

    echo "<br>";

    var_dump($valor !== $valor2);// se sao tipos diferentes
